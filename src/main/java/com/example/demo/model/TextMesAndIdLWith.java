package com.example.demo.model;

public class TextMesAndIdLWith {
	private String textMes;
	private Long idLWith;
	
	public TextMesAndIdLWith() {
		super();
	}

	public TextMesAndIdLWith(String textMes, Long idLWith) {
		super();
		this.textMes = textMes;
		this.idLWith = idLWith;
	}

	public String getTextMes() {
		return textMes;
	}

	public void setTextMes(String textMes) {
		this.textMes = textMes;
	}

	public Long getIdLWith() {
		return idLWith;
	}

	public void setIdLWith(Long idLWith) {
		this.idLWith = idLWith;
	}


	
}
